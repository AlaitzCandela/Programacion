    private void TFusuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TFusuarioActionPerformed
        try{
            boolean t=true;
            if(TFusuario.getText().length()<2){
                throw new DatoNoValido();  
                
            }
            Pattern usuario=Pattern.compile("[A-z]+([A-z]+)*$");
            Matcher musuario=usuario.matcher(TFusuario.getText());
            if(!musuario.matches()){
                throw new DatoNoValido();
            }
            t=Controlador.Usuario(TFusuario.getText());
            if(t==false){
                throw new DatoNoEncontrado();
            }
            else{
                TFContraseña.requestFocus();
            }
            
        }
        catch(DatoNoValido e){
            JOptionPane.showMessageDialog(this,"El usuario o la contraseña están mal");
            intentos+=1;
            if(intentos==3){
                JOptionPane.showMessageDialog(this,"No puedes utilizar esta plataforma");
                Controlador.Salir();
            }
            else{
                 JOptionPane.showMessageDialog(this,"Intentalo de nuevo");
            }
        }
        catch(DatoNoEncontrado e){
            JOptionPane.showMessageDialog(this,"El usuario o la contraseña están mal");
            intentos+=1;
            if(intentos==3){
                JOptionPane.showMessageDialog(this,"No puedes utilizar esta plataforma");
                Controlador.Salir();
            }
            else{
                 JOptionPane.showMessageDialog(this,"Intentalo de nuevo");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getClass()+e.getMessage());
            intentos+=1;
            if(intentos==3){
                JOptionPane.showMessageDialog(this,"No puedes utilizar esta plataforma");
                Controlador.Salir();
            }
            else{
                 JOptionPane.showMessageDialog(this,"Intentalo de nuevo");
            }
        }
    }//GEN-LAST:event_TFusuarioActionPerformed

    private void TFContraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TFContraseñaActionPerformed
        try{
            boolean t=true;
            if(TFContraseña.getText().length()>9){
                throw new DatoNoValido();
            }
            Pattern contraseña=Pattern.compile("[0-9]{8}[A-Z]$");
            Matcher mcontra=contraseña.matcher(TFContraseña.getText());
            if(!mcontra.matches()){
                throw new DatoNoValido();
            }
            t=Controlador.Contraseña(TFContraseña.getText());
            if(t==false){
                throw new DatoNoValido();
            }
            else{
                BAceptar.setVisible(true);
                BSalir.setVisible(true);
            }
        }
        catch(DatoNoValido e){
             JOptionPane.showMessageDialog(this,"El usuario o la contraseña están mal");
             intentos+=1;
            if(intentos==3){
                JOptionPane.showMessageDialog(this,"No puedes utilizar esta plataforma");
                Controlador.Salir();
            }
            else{
                 JOptionPane.showMessageDialog(this,"Intentalo de nuevo");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getClass()+e.getMessage());
            intentos+=1;
            if(intentos==3){
                JOptionPane.showMessageDialog(this,"No puedes utilizar esta plataforma");
                Controlador.Salir();
            }
            else{
                 JOptionPane.showMessageDialog(this,"Intentalo de nuevo");
            }
        }
    }//GEN-LAST:event_TFContraseñaActionPerformed

